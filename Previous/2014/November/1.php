<h2>1st November 2014</h2>

<p>In this week's café, anyone can come and do a 3 minute talk about an interesting
    piece of science news or research in order to persuade the rest of the group
    that your science is the most interesting!</p>

<p>There is a Facebook event
    <a href="https://www.facebook.com/events/760115060716272/">here!</a></p>

<p>You can either use something that you've found, or use one of our bank articles.
    Alternatively, please feel free to just come along, listen to other people's
    talks and to learn about some interesting bits of science!</p>

<p>Clearly, you'll be competing for the pride of winning a competition at the café,
    but there will also be some prizes available (ok, one prize available) for
    the winner.</p>

<p>There will be no speaker this week, it is a purely discussion-based café. We are
    looking forward to seeing you there at the usual time of 3:45 pm in Empty Shop!
    If you're unsure how to get there, please take a look at our
    <a href="index.php">homepage</a> where there is a map. On the day, we will put
    a blackboard outside at 3:30 to guide you in!</p>