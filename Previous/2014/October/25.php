<h2>25th October 2014</h2>

<p>In this talk, Liam Norman will cover some of the more interesting aspects of
colour vision, and how these aspects tell us about how colour vision works, from
the mechanisms of low-level photoreceptors in the eye to the processes in the
brain that determine that colours that we actually become conscious of.</p>

<p>As always, we will be meeting in the Empty Shop, which is just outside The
Gates Shopping Centre. There will be a board outside so you know where you are going! The 'doors' will open at 3:45 pm and the talk will be starting at 4:00 pm.
See you there!</p>

